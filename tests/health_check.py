def test_concurrency(base_url: str = "http://localhost:8001") -> bool:
    """Test concurrent request handling"""
    try:
        import threading
        import concurrent.futures
        
        def make_request():
            try:
                response = requests.get(f"{base_url}/health", timeout=5)
                return response.status_code == 200
            except:
                return False
        
        # Test 10 concurrent requests
        with concurrent.futures.ThreadPoolExecutor(max_workers=10) as executor:
            futures = [executor.submit(make_request) for _ in range(10)]
            results = [future.result() for future in concurrent.futures.as_completed(futures)]
        
        success_count = sum(results)
        if success_count >= 8:  # Allow for some failures
            print(f"âœ… Concurrency test passed: {success_count}/10 requests successful")
            return True
        else:
            print(f"âŒ Concurrency test failed: only {success_count}/10 requests successful")
            return False
            
    except Exception as e:
        print(f"âŒ Concurrency test failed: {e}")
        return False

